package com.flixclusive.core.presentation.player.ui

/**
 * Events that can be triggered in Picture-in-Picture (PiP) mode.
 * These events correspond to user actions such as play, pause, replay, backward, and forward.
 * */
enum class PiPEvent {
    PLAY,
    PAUSE,
    REPLAY,
    BACKWARD,
    FORWARD,
}
